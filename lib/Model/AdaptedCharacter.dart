class AdaptedCharacter {
  final String id;
  final String name;
  final String image;
  final String status;
  final String species;
  final String gender;
  final String origin;
  final String location;
  final int episode;

  AdaptedCharacter({
    required this.id,
    required this.name,
    required this.image,
    required this.status,
    required this.species,
    required this.gender,
    required this.origin,
    required this.location,
    required this.episode,
  });
}
